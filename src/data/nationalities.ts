export function getFlagEmoji(code: string): string {
  if (!code || code.includes("-")) return "🏳️";
  const upper = code.toUpperCase();
  return String.fromCodePoint(...[...upper].map(c => 0x1F1E6 + c.charCodeAt(0) - 65));
}
